String::String(void)
{
	length = 4;
	capacity = 10;
	str = new char[length];
	str[0] = 't';
	str[1] = 'i';
	str[2] = 't';
	str[3] = 'o';
}

String::String(const String& sentence)
{
	length = sentence.length();
	str = new char[length];
	str = sentence.GetStr();
}

// ===========================================================================
//                                  Destructor
// ===========================================================================
String::~String(void)
{
	delete str;
}

