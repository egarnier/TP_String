int String::MAX_SIZE=65535;
// ===========================================================================
//                                  Constructors
// ===========================================================================
String::String(void)
{
	length_ = 4;
	capacity_ = 10;
	str = new char[length_];
	str[0] = 't';
	str[1] = 'i';
	str[2] = 't';
	str[3] = 'o';
}

// ===========================================================================
//                                  Destructor
// ===========================================================================
String::~String(void)
{
	delete(str);
}

// ===========================================================================
//                                 Public Methods
// ===========================================================================
// Getters
int String::GetMAX_SIZE(void) const
{
	return MAX_SIZE;
}

int String::length(void) const
{
	return length_;
}

int String::size(void) const
{
	return length_;
}
int String::capacity(void) const
{
	return capacity_;
}

char* String::c_str(void) const
{
	return str;
}

